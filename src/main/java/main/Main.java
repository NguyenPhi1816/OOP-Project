    public boolean userAuthenciation(String userName, String userPassword) throws SQLException{
        ResultSet result = statement.executeQuery("SELECT * FROM TAIKHOAN");
        while(result.next()) {
            if (userName.equals(result.getString(1)) && userPassword.equals(result.getString(2))) {
                this.currentUser = new User(result.getString(1), result.getString(2), Integer.parseInt(result.getString(3)));
                return true;
            }
        }
        return false;
    }
    
    public void loadingRoomList() throws SQLException{
        if (this.roomList.layDanhSach().isEmpty()) {
            ResultSet result = statement.executeQuery("SELECT * FROM PHONG");
            
            while(result.next()) {
                Phong p = new Phong(result.getString(1), Integer.parseInt(result.getString(2)), result.getString(1));
                this.roomList.themPhong(p);
            }
        }
        
        for(Phong p: roomList.layDanhSach()) {
            this.btnMap.get(p.getMaPhong()).setBackground(ROOM_STATUS_COLOR[p.getTrangThai()]);
        }
    }
    
